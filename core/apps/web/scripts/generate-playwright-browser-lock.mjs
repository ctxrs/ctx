#!/usr/bin/env node

import { createHash } from "node:crypto";
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "../../../..");

export const defaultHostPlatforms = Object.freeze([
  "mac15-arm64",
  "ubuntu24.04-x64",
]);

export const defaultOutputPath = path.join(
  repoRoot,
  "tools",
  "bazel",
  "playwright_browser_runtime_lock.generated.bzl",
);
const supportedBrowsers = Object.freeze(["chromium", "chromium-headless-shell", "ffmpeg", "firefox", "webkit"]);

export const usage = () => [
  "usage: node core/apps/web/scripts/generate-playwright-browser-lock.mjs \\",
  "  [--output <path>] [--host-platform <platform> ...]",
].join("\n");

const readOptionValue = (args, option) => {
  const value = String(args.shift() ?? "").trim();
  if (!value || value.startsWith("--")) {
    throw new Error(`missing value for ${option}\n${usage()}`);
  }
  return value;
};

export const parseArgs = (argv) => {
  const parsed = {
    hostPlatforms: [],
    outputPath: defaultOutputPath,
  };
  const args = [...argv];
  while (args.length > 0) {
    const arg = args.shift();
    switch (arg) {
      case "--host-platform":
        parsed.hostPlatforms.push(readOptionValue(args, "--host-platform"));
        break;
      case "--output":
        parsed.outputPath = path.resolve(readOptionValue(args, "--output"));
        break;
      default:
        throw new Error(`unsupported argument: ${arg}\n${usage()}`);
    }
  }
  parsed.hostPlatforms = parsed.hostPlatforms.filter(Boolean);
  if (parsed.hostPlatforms.length === 0) {
    parsed.hostPlatforms = [...defaultHostPlatforms];
  }
  return parsed;
};

const resolvePlaywrightInfo = (playwrightRoot, hostPlatform, browserName) => {
  const nodeProgram = `
const path = require("node:path");
const playwrightRoot = process.argv[1];
const browserName = process.argv[2];
const { registry } = require(path.join(playwrightRoot, "lib/server/registry/index.js"));
const executable = registry.findExecutable(browserName);
if (!executable) {
  throw new Error("missing Playwright executable for " + browserName);
}
process.stdout.write(JSON.stringify({
  browserName,
  directoryName: path.basename(executable.directory),
  url: executable.downloadURLs[0],
}) + "\\n");
`;
  return JSON.parse(execFileSync(
    process.execPath,
    ["-e", nodeProgram, playwrightRoot, browserName],
    {
      encoding: "utf8",
      env: {
        ...process.env,
        PLAYWRIGHT_HOST_PLATFORM_OVERRIDE: hostPlatform,
      },
    },
  ));
};

const computeSha256 = async (url) => {
  const response = await fetch(url);
  if (!response.ok || !response.body) {
    throw new Error(`failed to download ${url}: ${response.status} ${response.statusText}`);
  }
  const hash = createHash("sha256");
  for await (const chunk of response.body) {
    hash.update(chunk);
  }
  return hash.digest("hex");
};

export const formatLockBzl = ({ generatedAt, playwrightVersion, entries }) => {
  const lines = [
    "# Generated by core/apps/web/scripts/generate-playwright-browser-lock.mjs",
    `# Generated at: ${generatedAt}`,
    `# Playwright version: ${playwrightVersion}`,
    "",
    "PLAYWRIGHT_BROWSER_RUNTIME_LOCK = {",
  ];
  for (const hostPlatform of Object.keys(entries).sort()) {
    lines.push(`    "${hostPlatform}": {`);
    const browsers = entries[hostPlatform];
    for (const browserName of Object.keys(browsers).sort()) {
      const entry = browsers[browserName];
      lines.push(`        "${browserName}": {`);
      lines.push(`            "directory_name": "${entry.directoryName}",`);
      lines.push(`            "sha256": "${entry.sha256}",`);
      lines.push(`            "url": "${entry.url}",`);
      lines.push("        },");
    }
    lines.push("    },");
  }
  lines.push("}");
  lines.push("");
  return lines.join("\n");
};

export const generateLockEntries = async ({
  hostPlatforms,
  playwrightRoot,
}) => {
  const entries = {};
  for (const hostPlatform of hostPlatforms) {
    const browserEntries = {};
    for (const browserName of supportedBrowsers) {
      const info = resolvePlaywrightInfo(playwrightRoot, hostPlatform, browserName);
      browserEntries[browserName] = {
        ...info,
        sha256: await computeSha256(info.url),
      };
    }
    entries[hostPlatform] = browserEntries;
  }
  return entries;
};

const main = async () => {
  const options = parseArgs(process.argv.slice(2));
  const playwrightRoot = path.join(repoRoot, "core", "node_modules", "playwright-core");
  const playwrightVersion = JSON.parse(
    fs.readFileSync(path.join(playwrightRoot, "package.json"), "utf8"),
  ).version;
  const entries = await generateLockEntries({
    hostPlatforms: options.hostPlatforms,
    playwrightRoot,
  });
  fs.mkdirSync(path.dirname(options.outputPath), { recursive: true });
  fs.writeFileSync(
    options.outputPath,
    formatLockBzl({
      entries,
      generatedAt: new Date().toISOString(),
      playwrightVersion,
    }),
  );
  process.stdout.write(`${options.outputPath}\n`);
};

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
  try {
    await main();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exit(1);
  }
}
